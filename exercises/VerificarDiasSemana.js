let diaSemana = 1

switch(diaSemana) {
    case 1:
        console.log('Hoje é domingo, fim de semana.')
        break
    case 2:
        console.log('Hoje é segunda-feira, dia útil.')
        break
    case 3:
        console.log('Hoje é terça-feira, dia útil.')
        break
    case 4:
        console.log('Hoje é quarta-feira, dia útil.')
        break
    case 5:
        console.log('Hoje é quinta-feira, dia útil.')
        break
    case 6:
        console.log('Hoje é sexta-feira, dia útil, sextou!!!!')
        break
    case 7:
        console.log('Hoje é sábado, fim de semana, sabadão sertanejo!!')
        break
    default:
        console.log('Dia inválido!')
}